class Ced_Mobiconnectcheckout_Block_Onepage_Billing extends Mage_Checkout_Block_Onepage_Billing
{
	public function _construct()
	{
		$params = $this->getRequest()->getParams();
		$checkmethod ='';
		if(isset($params['method'])){
			$checkmethod = $params['method'];
		}
		
		if($checkmethod =='guest')
		{
			Mage::getSingleton('customer/session')->unsetAll();
		}
		//print_r(Mage::getSingleton('customer/session')->getCustomerId()); die("dkdf");
	}
    public function getAddressesHtmlSelect($type)
    {
        if ($this->isCustomerLoggedIn()) {
            $options = array();
            foreach ($this->getCustomer()->getAddresses() as $address) {
                $options[] = array(
                    'value' => $address->getId(),
                    'label' => $address->format('html')
                );
            }

            $addressId = $this->getAddress()->getCustomerAddressId();
            if (empty($addressId)) {
                if ($type=='billing') {
                    $address = $this->getCustomer()->getPrimaryBillingAddress();
                } else {
                    $address = $this->getCustomer()->getPrimaryShippingAddress();
                }
                if ($address) {
                    $addressId = $address->getId();
                }
            }

            $select = $this->getLayout()->createBlock('core/html_select')
                ->setName($type.'_address_id')
                ->setId($type.'-address-select')
                ->setClass('address-select')
                ->setExtraParams('onchange="'.$type.'.newAddress(!this.value)"')
                ->setValue($addressId)
                ->setOptions($options);

            $select->addOption('', Mage::helper('checkout')->__('New Address'));

            return array('options'=> $options,'type'=>$type,'default_value'=>$addressId);
        }
        return '';
    }

}
